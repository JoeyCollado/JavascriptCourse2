fruits.pop(); //removes last element

fruits.unshift("mango"); //add element to the beginning

fruits.shift(); //remove the first element

let length = fruits.length; //checks the length of the elements

let index = fruits. indexOf("apple"); //finds the index of an element



console.log(index); //prints the result of indexOf method

console.log(length); //prints the result of length method

